# 1 - List DataType

### 1) Structure -
# - A list is a Collection of elements stored in a single variable.
# - List are denoted by square brackets [].
# Example -

my_list = [10, 20, 30, 40, 50]
print(my_list)
print(type(my_list))

### 2) Types of Data - (Heterogeneous)
# List can be stored different types of data (Heterogeneous)
# It can contain integers, floats, strings even other lists.
# example

my_list = [10, 3.24, True, "Hello"]
print(my_list)
print(type(my_list))

### 3) Sequence - (ordered)
# A list is an ordered squence 
# each element has a fixed position in list
# example -

my_list = [10, 20, 3.15, 'a', True]
print(my_list)
print(type(my_list))

