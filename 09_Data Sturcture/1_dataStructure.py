### Data Stucture (keypoints) -
# 1) Structure - 
# - Structure is refer to how data is organized, stored and arranged in memory.so that it can be effeiciently accessed and used.


### 2) Types of data -
# - which types of data stored in the structure.
# i) - Homogeneous => all elements are of the same type. 
# (ex - list of integers [1, 2, 3, 4], string["hello", "world"], float[1.1, 2.2, 3.3])

# ii) - Heterogeneous => all elements are of different type.
# (ex - list of different types[1, "hello", 3.14,  True])


### 3) Sequence -
# - Sequence means ther order of elements matters.
# i) Ordered => The sequence of elements will reamin as it is it will not change automatically.
# - elements arrange in a specific order

# ii) Unordered => elements are not arranged in a specific order.
# - position does not matters.
# - order can change automatically.


### 4) Changeable - 
# - Data structure are chnageable or not.
# i) Mutable => You can change the data after the creations.

# ii) Immutable => once created data cannot be chnaged


