# print() function in python
# This is the one function which is used to display the output on terminal.
# Inbulid function
# Multiple value can pass through to print function

a = 10
b = 20
c = 30
print(a, b, c)  # it will print all the value with space in between.
print('a:', a, 'b:', b, 'c:', c)  # it will print with label
print(f'a: {a}, b: {b}, c: {c}')  # f string
print('a:{} b{} c{}'.format(a, b, c))  # format method

nm = "Neha"
str = f'Name:{nm}'
print(str)  # it will print the string value
