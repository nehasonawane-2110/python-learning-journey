# operaters
# An operaters is an symbol that perform the operation on one or
# more value called as operaters.

a = 10
b = 30
c = 'abc'
d = 'def'


# Arithmetic operators - used for basic mathematical operations.
# 1. Addition (+)
print(a + b)
# Concatenation  - combining item end to end (abc + def =abcdef)
print(c + d)
# 2. Subtraction (-)
print(a - b)
# 3. Multiplication (*)
print(a * b)
# 4. Division (/)
print(a / b)
# 5. Modulus (%)
print(a % b)
# 6. Exponentiation (**)
print(a ** b)
# 7. Floor Division (//)
print(a // b)
