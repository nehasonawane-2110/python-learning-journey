# Comparison Operators
"""Used to compare two values and return a boolean result (True or False)."""

# 1. Exactly equal (==)
print(b == 30)
# 2. Not equal (!=)
print(a != b)
# 3. greater than >
print(a > b)
# 4 greater than equal >= ( grater than or equal to)
print(a >= 10)
# 5. less than <
print(a < b)
# 6. less then equal <= (less than or equal to)
print(a <= b)
