# BitWise operaters
# bitwise operaters woks on the binary numbers 0's and 1's

# 1. Bitwise AND (&).
print(5 & 3)
print(10 & 12)

# 2. Bitwise OR (|) - vertical Bar symbol
print(5 | 3)


# 3. Bitwise XOR (^)
print(5 ^ 3)

# 4. Bitwise NOT (~)  (used 2's compliment)
print(~+10)  #  -(10 + 1) = -11
print(~-10)  # -(-10 + 1) = -9     -(-9)= 9


# 5 Bitwise Left Shift operaters (<<)
print(5 << 2)
print(15 << 1)


# 6 bitwise right shift operatoer (>>)
print(5 >> 2)
print(22 >> 1)
