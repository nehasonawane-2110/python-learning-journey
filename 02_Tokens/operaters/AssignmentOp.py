# Assignment Operators
# Used to assign values to variables.
# 1. Assignment  Assign (=)
a = 10
# 2. Add and assign (+=)
a += b  # equivalent to a = a + b
# 3. Subtract and assign (-=)
a -= b  # a = a - b
# 4. Multiply and assign (*=)
a *= b  # equivalent to a = a * b
# 5. Divide and assign (/=)
a /= b
# 6. Modulus and assign (%=)
a %= b
# 7. Exponentiation and assign (**=)
a **= b
# 8. Floor division and assign (//=)
a //= b
print(a)
