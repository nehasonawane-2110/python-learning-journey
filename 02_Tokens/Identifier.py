# identifier

# identifier is a name used to identify variable, function ,class,modules. or object in python
# An identifier is any name you give to something in your code

# Example
age = 20  # age is identifier.
Name = "Neha"  # Name is identifier.

# Rules 
# can only contain letter (A -Z) (a-z) digit (0-9) and underscores(_)
# cannot start with digit
# connot used keyword
# case sensitive (name, Name,NAME) both are the different
# valid identifer are =  my_name, student1, _score , age