# write the program to check the max number form two numbers.

num1 = int(input("Enter the number 1: "))  # take input from user
num2 = int(input("Enter the number 2: "))

if (num1 > num2):  # compare two numbers
    print(f'{num1}  is the max number.')   # print the max number form two numbers
else:
    print(f'{num2} is the max number.')
